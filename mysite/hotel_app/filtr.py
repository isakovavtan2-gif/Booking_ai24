from django_filters.rest_framework import FilterSet
from .models import Hotel, Room


class HotelFilter(FilterSet):
    class Meta:
        model = Hotel
        fields = {
            'country': ['exact'],
            'city': ['exact'],
            'services': ['exact'],
        }

class RoomFilter(FilterSet):
    class Meta:
        model = Room
        fields = {
            'price': ['gt', 'lt'],
            'room_type': ['exact'],
            'room_status':['exact']
        }

